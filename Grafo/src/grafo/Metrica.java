package grafo;

/**
 *
 * @author Jefferson
 */
public class Metrica {
    private int A;
    private int B;
    private int C;
    private int Id;

    
    public Metrica(){}
    /**
     * @return the A
     */
    public int getA() {
        return A;
    }

    /**
     * @param A the A to set
     */
    public void setA(int A) {
        this.A = A;
    }

    /**
     * @return the B
     */
    public int getB() {
        return B;
    }

    /**
     * @param B the B to set
     */
    public void setB(int B) {
        this.B = B;
    }

    /**
     * @return the C
     */
    public int getC() {
        return C;
    }

    /**
     * @param C the C to set
     */
    public void setC(int C) {
        this.C = C;
    }

    /**
     * @return the Id
     */
    public int getId() {
        return Id;
    }

    /**
     * @param Id the Id to set
     */
    public void setId(int Id) {
        this.Id = Id;
    }
}
